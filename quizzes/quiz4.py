i = 0
while i<5:
  print(i)
  i += 1
else:
    print("Done")


    # This program uses a while loop to print the values of i from 0 to 4, inclusive.
# The loop continues until i is no longer less than 5.
# The print() function is used to output the value of i to the console.
# The variable i is incremented by 1 in each iteration using the += operator.
# After the loop completes, the else block is executed and prints "Done" to the console.


