# Define a variable called a and assign it the list [1, 2, 3, 4].
a = [1, 2, 3, 4]

# Define a variable called b and assign it the same value as a (i.e., a reference to the same list).
b = a

# Modify the first element of b to be 0.
# Since a and b refer to the same list, this also modifies the first element of a.
b[0] = 0

# Print the contents of the list a to the console.
# The output should be [0, 2, 3, 4], since the first element of a has been modified by the previous line of code.
print(a)


