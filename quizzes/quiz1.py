def foo(x):
    if x == 1:
        return 1
    else:
      return x * foo(x - 1)
print(foo(5))


# This program defines a recursive function 'foo' that calculates the factorial of a given number.
# The function takes an integer argument 'x'.
# If x is equal to 1, the function returns 1 (the base case of the recursion).
# Otherwise, the function returns the product of x and the result of calling itself recursively with the argument x - 1.
# The function is called with the argument 5.
# The return value of the function, which is the factorial of 5, is printed to the console using the print() function.





