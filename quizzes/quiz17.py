# Define a variable called a and assign it the string value "python is awesome".
a = "python is awesome"

# Use the find() method to search for the substring "is" in the value of a.
# The find() method returns the index of the first occurrence of the substring, or -1 if the substring is not found.
# In this case, the substring "is" is found at index 7 in the string "python is awesome".
print(a.find("is"))


