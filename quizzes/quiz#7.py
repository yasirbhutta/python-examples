i = 1

while i < 10:

   print(i)


   i += 2

   # This program prints odd numbers from 1 to 9, inclusive.
# The variable i is initialized to 1.
# The while loop continues until i is no longer less than 10.
# The print() function is used to output the value of i to the console.
# The variable i is incremented by 2 in each iteration using the += operator.


