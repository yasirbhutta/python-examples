for i in range(5):
    if i == 3:
        continue 
    print(i)


    # This program prints the values of i from 0 to 4, except for the value 3.
# The for loop iterates over a range of numbers from 0 to 4, inclusive of both endpoints.
# The if statement checks if the current value of i is equal to 3.
# If i is equal to 3, the continue statement is executed, which skips the current iteration of the loop.
# If i is not equal to 3, the print() function is used to output the value of i to the console.


