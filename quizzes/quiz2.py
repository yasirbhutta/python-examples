x = 10
y = 5
x = x + y
y = x - y
x = x - y
print(x, y)

# This program swaps the values of two variables without using a third variable.
# The variables x and y are initialized with the values 10 and 5, respectively.
# The values of x and y are swapped using arithmetic operations.
# First, the value of x is updated to the sum of x and y.
# Next, the value of y is updated to the difference between the new value of x and the original value of y.
# Finally, the value of x is updated to the difference between the new value of x and the new value of y.
# The print() function is used to output the new values of x and y to the console.


