for i in range(5):
  if i == 3:
    break
  print(i)


  # This program prints the values of i from 0 to 2.
# The for loop iterates over a range of numbers from 0 to 4, inclusive of both endpoints.
# The if statement checks if the current value of i is equal to 3.
# If i is equal to 3, the break statement is executed, which terminates the loop.
# If i is less than 3, the print() function is used to output the value of i to the console.

