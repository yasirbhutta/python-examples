# Define a variable called a and assign it the list [1, 2, 3].
a = [1, 2, 3]

# Define a variable called b and assign it the list [4, 5, 6].
b = [4, 5, 6]

# Use the + operator to concatenate the lists a and b into a new list called c.
c = a + b

# Print the contents of the list c to the console.
# The output should be [1, 2, 3, 4, 5, 6].
print(c)


