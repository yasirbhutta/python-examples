# Define two variables called x and y and assign them the values 10 and 5, respectively.
x = 10
y = 5

# Check if the value of x is equal to the value of y plus 2.
if x == y + 2:
    # If the condition is true, print "True" to the console.
    print("True")
else:
    # If the condition is false, print "False" to the console.
    print("False")

