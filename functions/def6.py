# Define a function called "my_func" that takes any number of arguments using the special syntax "*args".
def my_func(*args):
    # Iterate over each argument in "args".
    for arg in args:
        # Print the current argument to the console.
        print(arg)

# Call the "my_func" function with three arguments: 'hello', 'world', and 123.
# The output should be:
# hello
# world
# 123
my_func('hello', 'world', 123)


