def greet(name="friends"):
    print("Hello," + name + "!")
greet()


# This program defines a function called 'greet' that takes an optional string argument 'name'.
# If 'name' is not specified when the function is called, it defaults to the string "friends".
# The function prints a greeting message that includes the value of 'name'.
# The greeting message says "Hello," followed by the value of 'name', followed by an exclamation mark.
# If no argument is passed to the function, the greeting message will say "Hello, friends!".
# The function call 'greet()' does not specify an argument, so the greeting message will say "Hello, friends!".


