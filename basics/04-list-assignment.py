# List Assignment

nudge = 1                      # Basic assignment 
wink  = 2                      
A, B = nudge, wink             # Tuple assignment 
print( A, B )                  # Like A = nudge; B = wink 

[C, D] = [nudge, wink]         # List assignment 
print( C, D )