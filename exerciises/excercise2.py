def fun(arr):
    arr = arr[::-1]
    print(arr)
arr = [1, 2, 3, 4, 5]
fun(arr)


# This program defines a function called 'fun' that takes a list as its argument.
# The function reverses the order of the elements in the list using the slice notation 'arr[::-1]'.
# The reversed list is then printed to the console using the 'print' function.
# The program creates a list called 'arr' containing the numbers 1, 2, 3, 4, and 5.
# The function 'fun' is called with 'arr' as its argument, so the function will reverse the order of the elements in 'arr' and print the result to the console.
# The output of the program will be the reversed list [5, 4, 3, 2, 1] printed to the console.


