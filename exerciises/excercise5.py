for i in range(10):
    print(i)

# This program uses a for loop to iterate over the numbers from 0 to 9.
# The 'range' function is used to generate a sequence of numbers from 0 to 9.
# The 'for' loop is used to iterate over each number in the sequence.
# The 'print' function is used to output each number to the console.
# The output of the program will be the numbers 0 to 9 printed to the console, each on a new line.




