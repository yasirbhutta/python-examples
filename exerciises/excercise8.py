# Assign an integer value to n
n = 7531

# Initialize a variable to store the count of digits to 0
count = 0

# Loop until n becomes 0
while n > 0:
    # Divide n by 10 to remove the last digit
    n = n // 10
    # Increment the count of digits by 1
    count = count + 1

# Print the count of digits
print(count)





