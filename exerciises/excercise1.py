for i in range(5):
    if i == 3:
        continue
    print(i)


# This program uses a for loop to iterate over the range of numbers from 0 to 4.
# For each number, the program checks if the number is equal to 3 using an if statement.
# If the number is equal to 3, the program skips that iteration of the loop using the 'continue' keyword.
# If the number is not equal to 3, the program prints the number using the 'print' function.
# The output of the program will be the numbers 0, 1, 2, and 4 printed on separate lines.



