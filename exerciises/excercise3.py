a = 10
b = 20
print(str(a)+str(b))



# This program defines two variables 'a' and 'b' with the values 10 and 20, respectively.
# The program then concatenates the two variables as strings using the 'str' function and the '+' operator.
# The concatenated string is then printed to the console using the 'print' function.
# The output of the program will be the string "1020" printed to the console.



