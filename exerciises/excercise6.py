a = ('34.5',)
print(type(a))

# This program defines a tuple 'a' with a single element of the string "34.5".
# The 'type' function is used to determine the data type of 'a'.
# The 'print' function is used to output the data type of 'a' to the console.
# The output of the program will be "<class 'tuple'>" printed to the console.

