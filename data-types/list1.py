# Python data types - list

mylist =["chair", "table", "lamp","book"]
print(mylist)   ## Output: ['chair', 'table', 'lamp']

## access 2nd list item
print(mylist[1]) ## Output: table

## negative indexing
print(mylist[-1]) ## Output: book

## List Length
print(len(mylist)) ## Output: 4
